#ifndef __TEMP_CONVERT_H
#define __TEMP_CONVERT_H

#include "string.h"
#include "uart.h"
#include "flash.h"
#include "mcu.h"
#include "adc.h"
#include "user.h"
#include "math.h"

//=======================构件使用步骤开始================================
//（1）将本构件中的emuart_recv函数放入相应的串口中断中，并保存返回值到全局变量中。
//（2）在主函数中调用emuart_init函数完成初始化。
//（3）在有发送数据需要时，根据需要调用emuart_send1、emuart_sendN
//          或emuart_send_string函数发送数据。
//（4）在主程序中，通过判断emuart_recv的返回值是否为零判断是否接收到数据并处理。
//=======================构件使用步骤结束================================

//============================================================================
//函数名称：TempTrans
//功能概要：将读到的mcu温度AD值转换为实际温度
//参数说明：mcu_temp_AD：通过adc_read函数得到的AD值
//函数返回：实际温度值
//============================================================================
float TempTrans(uint16_t mcu_temp_AD);

//============================================================================
//函数名称：TempRegression
//功能概要：将读到的外部热敏电阻AD值转换为实际温度
//参数说明：tmpAD：通过adc_read函数得到的AD值
//函数返回：实际温度值
//============================================================================
float TempRegression(uint16_t tmpAD);


#endif
